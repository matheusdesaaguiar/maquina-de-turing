class Operations {
    static sum(number1, number2) {
        return number1 + number2;
    }

    static subtraction(number1, number2) {
        return number1 - number2;
    }

    static multiply(number1, number2) {
        return number1 * number2;
    }

    static division(number1, number2) {
        return number1 / number2;
    }

    static module( number1, number2) {
        return number1 % number2;
    }
}

export default Operations;